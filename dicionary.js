function mudaliguagem(valor){
var language = valor.toLowerCase();
var divs = document.getElementsByTagName("label");
	for (var key in dictionary[language]) {
		var procura = dictionary[language][key];
		var substitui = key;
		for (i = 0; i < divs.length; ++i) {
			if (divs[i].classList.contains(substitui)) {
			divs[i].innerHTML = procura;
			}
		}	
	}
}